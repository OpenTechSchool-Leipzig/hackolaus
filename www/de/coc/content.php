    <h2>Code of Conduct</h2>
    <div class="coc">
        
        <h3>Zweck</h3>

        <p>
        Ein vorrangiges Ziel aller von nullaufeins e.V. organisierten
        Veranstaltungen, Hackathons und Meetups besteht darin, möglichst viele
        Menschen mit unterschiedlichstem Hintergrund willkommen zu heißen und
        einzubeziehen. Daher verpflichten wir uns, eine freundliche, sichere und
        einladende Umgebung für alle zu schaffen, unabhängig von Ethnie, Kultur,
        nationaler Herkunft, Hautfarbe, Einwanderungsstatus, sozialer und
        wirtschaftlicher Hintergründe, Bildungsstand, Geschlecht, sexueller
        Orientierung, Geschlechtsidentität und -ausdruck, Alter, Größe,
        Familienstand, politischen Überzeugungen, Religion sowie geistiger und
        körperlicher Fähigkeiten.
        </p>
        <p>
        Dieser Verhaltenskodex legt unsere Erwartungen an alle fest, die an
        unserer Community teilnehmen, sowie die Konsequenzen für inakzeptables
        Verhalten.
        </p>
        <p>
        Wir laden alle ein, die an unseren Veranstaltungen teilnehmen und Teil
        unserer Community sein möchten, uns dabei zu helfen, sichere und
        positive Erfahrungen für alle zu schaffen.
        </p>

        <h3>Förderung offener Beteiligung</h3>

        <p>
        Ein zusätzliches Ziel dieses Verhaltenskodex besteht darin, die offene
        Beteiligung zu fördern, indem wir Teilnehmende ermutigen, die Beziehung
        zwischen unseren Handlungen und deren Auswirkungen auf unsere Community
        zu erkennen und zu stärken. Communities spiegeln die Gesellschaften
        wider, in denen sie existieren. Positive Aktionen sind unerlässlich, um
        den vielen Formen der Ungleichheit und des Missbrauchs von Macht
        entgegenzuwirken, die in der Gesellschaft existieren. Wenn du jemanden
        siehst, der besondere Anstrengungen unternimmt, um sicherzustellen, dass
        unsere Community einladend und freundlich ist und alle Teilnehmenden
        ermutigt, ihren Beitrag in vollem Umfang zu leisten, möchten wir das
        gern wissen.
        </p>

        <h3>Erwartetes Verhalten</h3>

        <ul>
        <li>
            Nimm auf authentische und aktive Weise an unserer Community teil. So
            trägst du zur Gesundheit und Langlebigkeit der nullaufeins-Community
            bei.
        </li>
        <li>Sprich und handle rücksichts- und respektvoll.</li>
        <li>Versuch, zu kooperieren, anstatt Konflikte zu eskalieren.</li>
        <li>
            Unterlasse herabwürdigendes, diskriminierendes oder belästigendes
            Verhalten. Dies bezieht sich ebenfalls auf deine Wortwahl.
        </li>
        <li>
            Achte auf die Personen in deiner Umgebung. Informiere jemanden vom
            nullaufeins-Team, wenn du Verstöße gegen diesen Verhaltenskodex
            wahrnimmst, auch wenn sie unbedeutend erscheinen.
        </li>
        </ul>

        <h3>Unakzeptables Verhalten</h3>
        <p>
        Zu den inakzeptablen Verhaltensweisen gehören: einschüchternde,
        belästigende, beleidigende, diskriminierende, herabsetzende oder
        erniedrigende Äußerungen oder Handlungen von Teilnehmenden unserer
        Community online, vorort und in der Kommunikation unter vier Augen im
        Rahmen von Community Events. Die Veranstaltungsorte unserer Community
        können unter Umständen auch von anderen Menschen genutzt werden, die
        nicht direkt mit der Community verbunden sind. Bitte verhalte dich
        respektvoll gegenüber allen.
        </p>
        <p>
        Zu den Belästigungen gehören: verletzende oder vorurteilsbehaftete
        verbale oder schriftliche Kommentare in Bezug auf Geschlecht, sexuelle
        Orientierung, Ethnie, Religion, Behinderung; unangemessene Verwendung
        von Nacktheit und/oder sexuellen Bildern (einschließlich
        Präsentationsfolien); unangemessene Gewaltdarstellungen (einschließlich
        Präsentationsfolien); vorsätzliche Einschüchterung, Verfolgung oder
        Nachstellung; belästigendes Fotografieren oder Aufnehmen; anhaltende
        Störung von Vorträgen oder anderen Veranstaltungen; unangemessener
        Körperkontakt und unerwünschte sexuelle Aufmerksamkeit.
        </p>

        <h3>Konsequenzen bei inakzeptablem Verhalten</h3>
        <p>
        Inakzeptables Verhalten von Mitgliedern unserer Community,
        einschließlich Sponsoren und Personen mit Entscheidungsbefugnis, wird
        nicht geduldet. Von allen, die aufgefordert werden, inakzeptables
        Verhalten einzustellen, wird erwartet, dass sie dieser Aufforderung
        sofort nachkommen.
        </p>
        <p>
        Wenn ein Mitglied der Community ein inakzeptables Verhalten an den Tag
        legt, können die Organisatoren der Community alle Maßnahmen ergreifen,
        die sie für angemessen halten, bis hin zu einem vorübergehenden Verbot
        oder einem dauerhaften Ausschluss aus der Community ohne Vorwarnung (und
        ohne Rückerstattung im Falle einer bezahlten Veranstaltung).
        </p>

        <h3>Wenn du Zeuge oder Opfer von inakzeptablem Verhalten wirst</h3>
        <p>
        Wenn du Zeuge von inakzeptablem Verhalten wirst oder andere Bedenken
        hast, benachrichtige bitte so schnell wie möglich einen Organisator von
        nullaufeins. Am Ende dieser Seite findest du eine Kontaktmöglichkeit,
        die du nutzen kannst, wenn du Verstöße dieses Verhaltenskodexes
        feststellen solltest. Darüber hinaus stehen Community-Organisatoren zur
        Verfügung, um Mitgliedern dabei zu helfen, sich mit den örtlichen
        Strafverfolgungsbehörden in Verbindung zu setzen oder auf andere Weise
        denjenigen zu helfen, die sich durch inakzeptables Verhalten betroffen
        fühlen. Im Rahmen von persönlichen Veranstaltungen werden die
        Organisatoren auf Wunsch der in Not geratenen Person auch
        Begleitpersonen zur Verfügung stellen.
        </p>

        <h3>Umgang mit Beschwerden</h3>
        <p>
        Wenn du der Meinung bist, dass du fälschlicherweise oder
        ungerechtfertigterweise beschuldigt wurdest, gegen diesen
        Verhaltenskodex verstoßen zu haben, solltest du einen der
        Community-Organisatoren von nullaufeins benachrichtigen und deine
        Beschwerde kurz schildern. Deine Beschwerde wird im Einklang mit unseren
        geltenden Richtlinien behandelt.
        </p>

        <h3>Geltungsbereich</h3>
        <p>
        Wir erwarten von allen Community-Teilnehmern (bezahlte oder nicht
        bezahlte Mitwirkende, Sponsoren und andere Gäste), dass sie sich an
        diesen Verhaltenskodex halten, und zwar an allen Community-Treffpunkten
        - online und vorort - sowie bei allen persönlichen Gesprächen im Rahmen
        der Community-Events.
        </p>

