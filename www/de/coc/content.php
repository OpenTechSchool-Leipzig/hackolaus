        <h3>Code of Conduct Team</h3>
        <p>
        Wenn dir ein Verhaltenskodexverstoß widerfährt oder du Zeuge davon
        wirst, wie er jemand anderem widerfährt, wende dich bitte sofort an das
        Organisationsteam, entweder persönlich oder per E-Mail an
        <a href="mailto:events@nullaufeins.org?subject=CodeOfConduct%20Request">events@nullaufeins.org</a>. Die Organisatoren werden bei den Veranstaltungen oder Treffen
